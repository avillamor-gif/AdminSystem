-- Asset Categories Table (IT Equipment, Furniture, Vehicles, etc.)
CREATE TABLE IF NOT EXISTS asset_categories (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name VARCHAR(100) NOT NULL UNIQUE,
  description TEXT,
  icon VARCHAR(50), -- For UI display
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Asset Brands Table
CREATE TABLE IF NOT EXISTS asset_brands (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name VARCHAR(100) NOT NULL UNIQUE,
  description TEXT,
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Asset Vendors/Suppliers Table
CREATE TABLE IF NOT EXISTS asset_vendors (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  name VARCHAR(255) NOT NULL,
  contact_person VARCHAR(255),
  email VARCHAR(255),
  phone VARCHAR(50),
  address TEXT,
  is_active BOOLEAN DEFAULT true,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Main Assets Table
CREATE TABLE IF NOT EXISTS assets (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  asset_tag VARCHAR(50) UNIQUE, -- Unique identifier (e.g., AST-2024-001), auto-generated by trigger
  name VARCHAR(255) NOT NULL,
  category_id UUID REFERENCES asset_categories(id) ON DELETE SET NULL,
  brand_id UUID REFERENCES asset_brands(id) ON DELETE SET NULL,
  model VARCHAR(255),
  serial_number VARCHAR(255),
  
  -- Purchase Information
  vendor_id UUID REFERENCES asset_vendors(id) ON DELETE SET NULL,
  purchase_date DATE,
  purchase_price DECIMAL(12, 2),
  purchase_order_number VARCHAR(100),
  
  -- Warranty Information
  warranty_start_date DATE,
  warranty_end_date DATE,
  warranty_details TEXT,
  
  -- Depreciation
  useful_life_years INTEGER,
  salvage_value DECIMAL(12, 2),
  depreciation_method VARCHAR(50) CHECK (depreciation_method IN ('straight_line', 'declining_balance', 'none')),
  
  -- Status and Condition
  status VARCHAR(50) DEFAULT 'available' CHECK (status IN ('available', 'assigned', 'maintenance', 'retired', 'lost', 'damaged')),
  condition VARCHAR(50) CHECK (condition IN ('excellent', 'good', 'fair', 'poor')),
  
  -- Location and Assignment
  location VARCHAR(255),
  assigned_to UUID REFERENCES employees(id) ON DELETE SET NULL,
  assigned_date DATE,
  
  -- Additional Details
  specifications JSONB, -- Store detailed specs (RAM, Storage, etc.)
  qr_code TEXT,
  image_url TEXT,
  notes TEXT,
  
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Asset Assignment History
CREATE TABLE IF NOT EXISTS asset_assignments (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  asset_id UUID NOT NULL REFERENCES assets(id) ON DELETE CASCADE,
  employee_id UUID NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  assigned_by UUID REFERENCES employees(id) ON DELETE SET NULL,
  assigned_date DATE NOT NULL DEFAULT CURRENT_DATE,
  returned_date DATE,
  returned_by UUID REFERENCES employees(id) ON DELETE SET NULL,
  condition_on_assignment VARCHAR(50),
  condition_on_return VARCHAR(50),
  notes TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Asset Maintenance Records
CREATE TABLE IF NOT EXISTS asset_maintenance (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  asset_id UUID NOT NULL REFERENCES assets(id) ON DELETE CASCADE,
  maintenance_type VARCHAR(50) CHECK (maintenance_type IN ('repair', 'inspection', 'upgrade', 'cleaning', 'calibration')),
  scheduled_date DATE,
  completed_date DATE,
  performed_by VARCHAR(255),
  vendor_id UUID REFERENCES asset_vendors(id) ON DELETE SET NULL,
  cost DECIMAL(10, 2),
  description TEXT NOT NULL,
  notes TEXT,
  status VARCHAR(50) DEFAULT 'scheduled' CHECK (status IN ('scheduled', 'in_progress', 'completed', 'cancelled')),
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- Asset Requests (Employees requesting equipment)
CREATE TABLE IF NOT EXISTS asset_requests (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  employee_id UUID NOT NULL REFERENCES employees(id) ON DELETE CASCADE,
  category_id UUID REFERENCES asset_categories(id) ON DELETE SET NULL,
  item_description TEXT NOT NULL,
  justification TEXT,
  priority VARCHAR(50) DEFAULT 'normal' CHECK (priority IN ('low', 'normal', 'high', 'urgent')),
  status VARCHAR(50) DEFAULT 'pending' CHECK (status IN ('pending', 'approved', 'rejected', 'fulfilled')),
  requested_date DATE DEFAULT CURRENT_DATE,
  approved_by UUID REFERENCES employees(id) ON DELETE SET NULL,
  approved_date DATE,
  fulfilled_date DATE,
  assigned_asset_id UUID REFERENCES assets(id) ON DELETE SET NULL,
  rejection_reason TEXT,
  notes TEXT,
  created_at TIMESTAMPTZ DEFAULT NOW(),
  updated_at TIMESTAMPTZ DEFAULT NOW()
);

