-- Create sequence for asset tags
CREATE SEQUENCE IF NOT EXISTS asset_tag_seq START 1;

-- Function to auto-generate asset tags
CREATE OR REPLACE FUNCTION generate_asset_tag()
RETURNS TRIGGER AS $$
DECLARE
  next_number INTEGER;
  new_tag VARCHAR(50);
BEGIN
  -- Always generate if not provided or empty
  IF NEW.asset_tag IS NULL OR TRIM(NEW.asset_tag) = '' THEN
    -- Get next number from sequence
    next_number := nextval('asset_tag_seq');
    
    -- Generate new tag: AST-00001
    NEW.asset_tag := 'AST-' || LPAD(next_number::TEXT, 5, '0');
  END IF;
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger to auto-generate asset tags
DROP TRIGGER IF EXISTS trigger_generate_asset_tag ON assets;
CREATE TRIGGER trigger_generate_asset_tag
  BEFORE INSERT ON assets
  FOR EACH ROW
  EXECUTE FUNCTION generate_asset_tag();

-- Function to update asset status when assigned/returned
CREATE OR REPLACE FUNCTION update_asset_on_assignment()
RETURNS TRIGGER AS $$
BEGIN
  IF TG_OP = 'INSERT' THEN
    -- Update asset status to assigned
    UPDATE assets
    SET 
      status = 'assigned',
      assigned_to = NEW.employee_id,
      assigned_date = NEW.assigned_date,
      updated_at = NOW()
    WHERE id = NEW.asset_id;
  ELSIF TG_OP = 'UPDATE' AND NEW.returned_date IS NOT NULL AND OLD.returned_date IS NULL THEN
    -- Update asset status to available when returned
    UPDATE assets
    SET 
      status = 'available',
      assigned_to = NULL,
      assigned_date = NULL,
      updated_at = NOW()
    WHERE id = NEW.asset_id;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Trigger to update asset status on assignment changes
DROP TRIGGER IF EXISTS trigger_update_asset_on_assignment ON asset_assignments;
CREATE TRIGGER trigger_update_asset_on_assignment
  AFTER INSERT OR UPDATE ON asset_assignments
  FOR EACH ROW
  EXECUTE FUNCTION update_asset_on_assignment();
